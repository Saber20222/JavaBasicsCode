 package com.syntax.class07;

public class WhileLoop {

	public static void main(String[] args) {
		
		
		int time=1;
		if(time<12) {
			System.out.println("Morning");
		}
		
		
		System.out.println("----------------------");
		
		while(time<=12) {
			System.out.println("Good Morning");
			time++;
		}
		
	}

}

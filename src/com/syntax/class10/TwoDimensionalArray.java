package com.syntax.class10;

public class TwoDimensionalArray {

	public static void main(String[] args) {
		// create 2d array
		int[][] numbers=new int[3][4];
		// 1 row
		numbers[0][0]=10;
		numbers[0][1]=20;
		numbers[0][2]=30;
		numbers[0][3]=40;
		// 2 row
		numbers[1][0]=1;
		numbers[1][1]=2;
		numbers[1][2]=3;
		numbers[1][3]=4;
		// 3 row
		numbers[0][0]=10;
		numbers[0][2]=10;
		numbers[0][3]=10;
		numbers[0][4]=10;
		
		System.out.println();
	}

}
